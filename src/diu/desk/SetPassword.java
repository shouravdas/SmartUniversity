    private void codeTfKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_codeTfKeyReleased
        try{
            
            pStatement=con.prepareStatement("SELECT recover_code from adminlogin WHERE email=?");
            pStatement.setString(1,receiverMail);
            rs=pStatement.executeQuery();
            while(rs.next()){
                recode=rs.getString("recover_code");
            }
            if(recode.equals(codeTf.getText())){
                pass.setEditable(true);
                cpass.setEditable(true);
                showText.setText("set new Password");
            }
            else{
                showText.setText("Enter Correct code");
            }
        }
        catch(Exception excp){
            System.out.println(excp);
        }
    }//GEN-LAST:event_codeTfKeyReleased

