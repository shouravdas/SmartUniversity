    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
       try{

            password1=new String(pass.getPassword());
            password2=new String(cpass.getPassword());
            generatepassword();
             if(password1.matches(password2)){
                
                pStatement=con.prepareStatement("UPDATE adminlogin SET password=?,salt_value=? WHERE email=?");
                pStatement.setString(1,mySecurePass);
                pStatement.setString(2,saltCode);
                pStatement.setString(3,receiverMail);
                pStatement.executeUpdate();
                
                JOptionPane.showMessageDialog(null, "Your New password is saved back to login");
                this.dispose();
                Login h=new Login();
                h.setVisible(true);
       }
             else{
                    JOptionPane.showMessageDialog(null,"Passward missmatch" );

               }
            }

            catch(Exception exc){
                System.out.println(exc);
            }
    }//GEN-LAST:event_saveBtnActionPerformed

