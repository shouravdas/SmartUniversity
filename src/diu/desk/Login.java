    void Login(){

        String password = new String(passwordTF.getPassword());
        try{
             String loginCheck="SELECT userName,password,salt_value,admin_type FROM adminlogin WHERE userName=? ";
             pStatement=con.prepareStatement(loginCheck);

                 pStatement.setString(1,userNameTF.getText());
                 rs=pStatement.executeQuery();

             if(rs.next()){
              String securepassword=rs.getString("password");
              String salt=rs.getString("salt_value");
                 user=rs.getString("userName");
                 String role=rs.getString("admin_type");

                 boolean passwordMatch=PasswordManager.verifyUserPassword(password,securepassword,salt);
                 if(passwordMatch){
                     PassUserName();
                 Home home=new Home(role);
                 home.NameLbl.setText("Welcome Mr. "+user);
                 home.setVisible(true);
                 this.dispose();

                 }
                 else{
                     JOptionPane.showMessageDialog(null, "Check your Pssword");
                 }
             }
              else{
                JOptionPane.showMessageDialog(null, "Check your user name");
             }

        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Error"+e);

        }
    }


