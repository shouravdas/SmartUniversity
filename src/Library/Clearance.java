    public void CheckBookInfo(){
     try {
         pStatement=con.prepareStatement("SELECT * FROM `book_borrow` WHERE library_id=?");
          pStatement.setString(1,library_id);
           rs=pStatement.executeQuery();
           while(rs.next()){
              String brdate=rs.getString("date_borrow");
              String redate=rs.getString("return_date");
               String bid=rs.getString("book_id");
              borroDate.add(brdate);
              returnDate.add(redate);
              bookid.add(bid);
           }
         
     } catch (SQLException ex) {
         Logger.getLogger(Clearance.class.getName()).log(Level.SEVERE, null, ex);
     }
    }
    
    public void ClearenceCheck(){
        
        for(int i=0;i<borroDate.size();i++){
            if((borroDate.get(i)!=null)&&(returnDate.get(i)==null)){
              try {
                pStatement=con.prepareStatement("SELECT * FROM `books` WHERE book_id=?");
                pStatement.setString(1,bookid.get(i));
                rs=pStatement.executeQuery();
                if(rs.next()){
                    String bookName=rs.getString("book_name");
                    String publisher=rs.getString("publisher");
                    String page=rs.getString("page");
                    String price=rs.getString("price");
                    ShowText.append("Book Name: "+bookName+'\n'+"Publisher: "+publisher+'\n'+"Page: "+page+'\n'+"Price: "+price+'\n'+'\n');
                  }
                jLabel2.setText("Book Need to Clear"); 
                    } catch (SQLException ex) {
                        Logger.getLogger(Clearance.class.getName()).log(Level.SEVERE, null, ex);
                    }
              
            }
            else{
            jLabel2.setText("No Book to Return");    
               }
        }
    }
    
    
    
