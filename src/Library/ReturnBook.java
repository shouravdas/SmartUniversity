public void SearchUser(){
    Student_id=null;  
    try {
           pStatement=con.prepareStatement("SELECT * FROM library_member WHERE library_id=?");
           pStatement.setString(1,studentIdTF.getText());
           rs=pStatement.executeQuery();
           if(rs.next()){
               Student_id=rs.getString("library_id");
               System.out.println(Student_id);
           }
       } catch (SQLException ex) {
           Logger.getLogger(StudentRege.class.getName()).log(Level.SEVERE, null, ex);
       }
        
    }
    public void SearchBook(){
      book_id=null;
        try {
           pStatement=con.prepareStatement("SELECT * FROM `books` WHERE book_id=?");
           pStatement.setString(1,bookIdTf.getText());
           rs=pStatement.executeQuery();
           if(rs.next()){
               book_id=rs.getString("book_id");  
                count=rs.getInt("available");
           }
       } catch (SQLException ex) {
           Logger.getLogger(StudentRege.class.getName()).log(Level.SEVERE, null, ex);
       }
        
    }
      public void UpdtadeQunatity(){
        
        try {
            pStatement=con.prepareStatement("update books set available=? where book_id=?");
            pStatement.setInt(1, count+1);
            pStatement.setString(2,bookIdTf.getText());
            pStatement.executeUpdate();
        } catch (SQLException ex) {
            Logger.getLogger(BorrowInfo.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
