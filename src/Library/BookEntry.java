    private void registerbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerbtnActionPerformed

            DateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
	Date dateGet = new Date(); 
        try {
           pStatement=con.prepareStatement("insert into books (book_name,page,publisher,price,book_quantity,available,date) values (?,?,?,?,?,?,?)");
           pStatement.setString(1, bookNameTF.getText());
           pStatement.setString(2, pagesTF.getText());
           pStatement.setString(3, publisherTF.getText());
           pStatement.setString(4, priceTF.getText());
           pStatement.setString(5, bookQuantity.getSelectedItem().toString());
           pStatement.setString(6, bookQuantity.getSelectedItem().toString());
          pStatement.setString(7,dateFormat.format(dateGet));
            
          pStatement.execute();
           new NoticeWindow(Color.green, "Data Saved", 003, NPosition.BOTTOM_RIGHT);
           ClearTF();
       } catch (SQLException ex) {
               new NoticeWindow(Color.red, "Error"+ex, 003, NPosition.BOTTOM_RIGHT);
       }
    }//GEN-LAST:event_registerbtnActionPerformed
        
