       public void BookSearchById(){
            book_id=null;
             ShowInfo.setText(null);
        try {
           pStatement=con.prepareStatement("SELECT * FROM `books` WHERE book_id=?");
           pStatement.setString(1,search.getText());
          
           rs=pStatement.executeQuery();
           if(rs.next()){
              book_id=rs.getString("book_id"); 
               String BookName=rs.getString("book_name");
               String Publisher=rs.getString("publisher");
               String page=rs.getString("page");
               String price=rs.getString("price");
               String quantity=rs.getString("book_quantity");               
               String available=rs.getString("available");
               ShowInfo.append("Book ID        : "+book_id+'\n'+"Book Name : "+BookName+'\n'+"Publisher     : "+Publisher+'\n'+
                       "Page No      : "+page+'\n'+"Price             : "+price+'\n'+"Quantity        : "+quantity+
                       '\n'+"Available      : "+available+'\n');
           }
       } catch (SQLException ex) {
           Logger.getLogger(StudentRege.class.getName()).log(Level.SEVERE, null, ex);
       }
       }
       
